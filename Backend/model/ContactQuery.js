const mongoose = require('mongoose');

const contactQuerySchema = new mongoose.Schema({
  firstname: {
    type: String,
    required: true,
    trim: true,
  },
  lastname: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    lowercase: true,
    trim: true,
  },
  phone: {
    type: String,
    required: true,
    trim: true,
  },
  subject: {
    type: String,
    required: true,
    trim: true,
  },
  message: {
    type: String,
    required: true,
    trim: true,
  },
  replied: {
    type: Boolean,
    default: false,
  },
}, {
  timestamps: true, 
});

module.exports = mongoose.model('ContactQuery', contactQuerySchema);